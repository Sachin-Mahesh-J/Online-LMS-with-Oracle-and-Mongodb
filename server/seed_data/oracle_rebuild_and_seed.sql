create or replace procedure enroll_student (
   p_student_id in enrollments.student_id%type,
   p_course_id  in enrollments.course_id%type
) as
   v_count number;
begin
   select count(*) into v_count from students where student_id = p_student_id;
   if v_count = 0 then
      raise_application_error(-20001, 'Student does not exist.');
   end if;

   select count(*) into v_count from courses where course_id = p_course_id;
   if v_count = 0 then
      raise_application_error(-20002, 'Course does not exist.');
   end if;

   select count(*) into v_count from enrollments where student_id = p_student_id and course_id = p_course_id;
   if v_count > 0 then
      raise_application_error(-20003, 'Student is already enrolled in this course.');
   end if;

   insert into enrollments (student_id, course_id, enrollment_date, completion_status, progress_percent)
   values (p_student_id, p_course_id, sysdate, 'ENROLLED', 0);
exception
   when dup_val_on_index then
      raise_application_error(-20003, 'Student is already enrolled in this course.');
end;
/
show errors;

create or replace procedure record_payment (
   p_enrollment_id  in payments.enrollment_id%type,
   p_amount         in payments.amount%type,
   p_payment_method in payments.payment_method%type,
   p_payment_status in payments.payment_status%type
) as
   v_count number;
begin
   select count(*) into v_count from enrollments where enrollment_id = p_enrollment_id;
   if v_count = 0 then
      raise_application_error(-20004, 'Enrollment does not exist.');
   end if;

   select count(*) into v_count
   from payments
   where enrollment_id = p_enrollment_id
     and payment_status = 'PAID';

   if v_count > 0 then
      raise_application_error(-20008, 'A successful payment already exists for this enrollment.');
   end if;

   insert into payments (enrollment_id, amount, payment_date, payment_method, payment_status)
   values (p_enrollment_id, p_amount, sysdate, p_payment_method, p_payment_status);
exception
   when dup_val_on_index then
      raise_application_error(-20008, 'A successful payment already exists for this enrollment.');
end;
/
show errors;

create or replace function get_student_total_payments (
   p_student_id in students.student_id%type
) return number as
   v_total number := 0;
begin
   select nvl(sum(p.amount), 0)
     into v_total
     from payments p
     join enrollments e on p.enrollment_id = e.enrollment_id
    where e.student_id = p_student_id
      and p.payment_status = 'PAID';

   return v_total;
end;
/
show errors;

create or replace trigger trg_update_enrollment_after_payment
after insert on payments
for each row
begin
   if :new.payment_status = 'PAID' then
      update enrollments
         set completion_status = 'IN_PROGRESS',
             progress_percent = case when progress_percent = 0 then 10 else progress_percent end
       where enrollment_id = :new.enrollment_id
         and completion_status = 'ENROLLED';
   end if;
end;
/
show errors;

create or replace trigger trg_check_certificate_completion
before insert on certifications
for each row
declare
   v_status enrollments.completion_status%type;
begin
   select completion_status into v_status
   from enrollments
   where enrollment_id = :new.enrollment_id;

   if v_status <> 'COMPLETED' then
      raise_application_error(-20005, 'Certificate can only be issued for completed enrollments.');
   end if;
end;
/
show errors;

create or replace procedure issue_certificate (
   p_enrollment_id    in certifications.enrollment_id%type,
   p_certificate_code in certifications.certificate_code%type,
   p_grade            in certifications.grade%type
) as
   v_count number;
begin
   select count(*) into v_count from enrollments where enrollment_id = p_enrollment_id;
   if v_count = 0 then
      raise_application_error(-20006, 'Enrollment does not exist.');
   end if;

   select count(*) into v_count from certifications where enrollment_id = p_enrollment_id;
   if v_count > 0 then
      raise_application_error(-20007, 'Certificate already exists for this enrollment.');
   end if;

   insert into certifications (enrollment_id, certificate_code, issue_date, grade, certificate_status)
   values (p_enrollment_id, p_certificate_code, sysdate, p_grade, 'ISSUED');
exception
   when dup_val_on_index then
      raise_application_error(-20007, 'Certificate already exists for this enrollment.');
end;
/
show errors;

